public class Vaccine 
{
    private String vaxType;
    private String vaxManu;
    private String vaxLot;
    private String vaxDoseSeries;
    private String vaxRoute;
    private String vaxSite;
    private String vaxName;

    public Vaccine()
    {
        // Do Nothing...
    }

    public String getVaxType()
    {
        return this.vaxType;
    }

    public void setVaxType(String vaxType)
    {
        this.vaxType = vaxType;
    }

    public String getVaxManu()
    {
        return this.vaxManu;
    }

    public void setVaxManu(String vaxManu)
    {
        this.vaxManu = vaxManu;
    }

    public String getVaxLot()
    {
        return this.vaxLot;
    }

    public void setVaxLot(String vaxLot)
    {
        this.vaxLot = vaxLot;
    }

    public String getVaxDoseSeries()
    {
        return this.vaxDoseSeries;
    }

    public void setVaxDoseSeries(String vaxDoseSeries)
    {
        this.vaxDoseSeries = vaxDoseSeries;
    }

    public String getVaxRoute()
    {
        return this.vaxRoute;
    }

    public void setVaxRoute(String vaxRoute)
    {
        this.vaxRoute = vaxRoute;
    }

    public String getVaxSite()
    {
        return this.vaxSite;
    }

    public void setVaxSite(String vaxSite)
    {
        this.vaxSite = vaxSite;
    }

    public String getVaxName()
    {
        return this.vaxName;
    }

    public void setVaxName(String vaxName)
    {
        this.vaxName = vaxName;
    }
}
